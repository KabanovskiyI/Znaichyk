/**
 * Функція для отримання даних нового раунду
 * @returns {Array} - Перемішаний масив об'єктів { text, isCorrect }
 */
export const generateBubbleRound = (realWords = REAL_WORDS, fakeWords = FAKE_WORDS) => {
    if (realWords.length < 1 || fakeWords.length < 2) {
        console.error("Недостатньо слів для генерації раунду");
        return [];
    }

    const correctWord = realWords[Math.floor(Math.random() * realWords.length)];

    const selectedFakes = [];
    const tempFakes = [...fakeWords];
    
    while (selectedFakes.length < 2 && tempFakes.length > 0) {
        const randomIndex = Math.floor(Math.random() * tempFakes.length);
        selectedFakes.push(tempFakes.splice(randomIndex, 1)[0]);
    }

    const roundWords = [
        { text: correctWord, isCorrect: true },
        { text: selectedFakes[0], isCorrect: false },
        { text: selectedFakes[1], isCorrect: false }
    ];

    return roundWords.sort(() => Math.random() - 0.5);
};